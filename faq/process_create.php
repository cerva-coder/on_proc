<h3><i class="bi bi-file-earmark-plus text-success"></i> Criação de Processos</h3>
<hr>

<p>
  A funcionalidade de <strong>criação de processos</strong> é uma etapa essencial do sistema <strong>ON_Proc</strong>, permitindo o registro formal de procedimentos administrativos no âmbito do seu departamento.
</p>

<p>
  Apenas o <strong>Administrador do Departamento</strong> (usuário com perfil <em>gerente</em> ou <em>admin</em>) possui permissão para realizar a criação de novos processos no sistema. Isso garante maior controle, organização e rastreabilidade sobre as movimentações documentais do Departamento.
</p>

<h5 class="mt-4">O que é necessário para criar um processo?</h5>
<ul>
  <li><strong>Assunto:</strong> breve descrição sobre o tema ou objeto do processo.</li>
  <li><strong>Polo Passivo:</strong> pessoa ou entidade diretamente vinculada ao processo (ex: civil, empresa).</li>
  <li><strong>Encarregado:</strong> nome da pessoa responsável pelo acompanhamento ou instrução do processo.</li>
  <li><strong>Tipo de Processo:</strong> categoria conforme a finalidade (ex: sindicância, PAD, requerimento etc.).</li>
</ul>

<p>
  O sistema também permite a escolha de <strong>"Outros"</strong> como tipo de processo, caso nenhuma das opções pré-definidas seja aplicável. Nesse caso, o Administrador deverá especificar manualmente a natureza do processo.
</p>

<h5 class="mt-4">Numeração Automática (NUP)</h5>
<p>
  Ao ser criado, o sistema gera automaticamente um número de protocolo (NUP) padronizado, baseado no código do Departamento e na ordem anual de registros. Isso garante unicidade e organização documental.
</p>

<h5 class="mt-4">Importante:</h5>
<div class="alert alert-warning">
  Esta etapa é de responsabilidade exclusiva do Administrador do Departamento. Os demais usuários poderão apenas visualizar ou interagir com processos já criados, conforme seu perfil de acesso.
</div>

<p class="mt-4">
  Em caso de dúvidas sobre qual tipo de processo utilizar, consulte previamente o responsável jurídico do Departamento ou o manual normativo vigente.
</p>
