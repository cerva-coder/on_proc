<h3><i class="bi bi-info-circle text-success"></i> Sobre o Sistema ON_Proc</h3>
<hr>

<p>
  O <strong>ON_Proc</strong> é um sistema desenvolvido para facilitar o controle, gestão e acompanhamento de <strong>processos administrativos</strong> no âmbito do seu Departamento.
</p>

<p>
  Com uma interface moderna e amigável, o sistema foi pensado para atender às necessidades de diferentes perfis de usuários — desde administradores até operadores e consultores ocasionais — assegurando integridade dos dados, rastreabilidade e segurança documental.
</p>

<h5 class="mt-4"><i class="bi bi-people-fill"></i> Perfis de Usuário</h5>
<ul>
  <li><strong>Administrador do Sistema</strong>: acesso total, inclusive a departamentos e usuários.</li>
  <li><strong>Administrador do Departamento (Gerente)</strong>: responsável pela gestão dos processos no âmbito de seu departamento.</li>
  <li><strong>Protocolador</strong>: pode protocolar documentos nos processos aos quais tem acesso.</li>
  <li><strong>Visualizador</strong>: acesso somente leitura aos processos e seus documentos.</li>
</ul>

<h5 class="mt-4"><i class="bi bi-diagram-3"></i> Funcionalidades Principais</h5>
<ul>
  <li><strong>Criação de Processos</strong>: feita exclusivamente pelos administradores do Departamento.</li>
  <li><strong>Protocolo de Documentos</strong>: usuários designados podem anexar documentos em PDF aos processos em andamento.</li>
  <li><strong>Andamento</strong>: visualização completa do histórico documental de cada processo.</li>
  <li><strong>Gestão de Usuários, Departamentos e Tipos de Processos</strong> (para administradores).</li>
  <li><strong>Download em lote</strong>: permite exportar todos os arquivos de um processo em formato ZIP.</li>
</ul>

<h5 class="mt-4"><i class="bi bi-shield-lock"></i> Segurança</h5>
<ul>
  <li>Todos os documentos são <strong>criptografados com AES-256</strong> antes do armazenamento.</li>
  <li>O sistema exige <strong>confirmação de senha</strong> para ações sensíveis como o protocolo.</li>
  <li><strong>Controle de acessos e permissões</strong> baseado no perfil do usuário e departamento.</li>
</ul>

