<h3><i class="bi bi-journal-text text-success"></i> Andamento do Processo</h3>
<hr>

<p>
  A página de <strong>andamento do processo</strong> oferece uma visão completa de todas as movimentações documentadas em um processo específico, além de exibir seus dados principais e permitir ações como visualizar ou baixar arquivos PDF anexados.
</p>

<h5 class="mt-4">O que pode ser visualizado?</h5>
<ul>
  <li><strong>NUP</strong> — Número Único do Processo, gerado automaticamente pelo sistema.</li>
  <li><strong>Assunto</strong>, <strong>Polo Passivo</strong> e <strong>Encarregado</strong> do processo.</li>
  <li><strong>Tipo de Processo</strong> e seu <strong>Status</strong> atual.</li>
  <li><strong>Lista cronológica de documentos</strong> já protocolados.</li>
</ul>

<h5 class="mt-4">Movimentações registradas</h5>
<p>
  Cada movimentação documentada aparece em forma de <strong>cartão individual</strong>, contendo:
</p>
<ul>
  <li><strong>Título da movimentação</strong> (ex: Relatório Preliminar, Notificação, Decisão etc.);</li>
  <li><strong>Descrição</strong> resumida do conteúdo anexado;</li>
  <li><strong>Nome do  responsável</strong> pelo envio e sua <strong>função/posto</strong>;</li>
  <li><strong>Data e hora do envio</strong>;</li>
  <li>Botões para <strong>visualizar o PDF no navegador</strong> ou <strong>fazer download</strong>.</li>
</ul>

<h5 class="mt-4">Ações disponíveis</h5>
<ul>
  <li><strong>Botão "Protocolar"</strong> — aparece apenas se o processo estiver <span class="badge bg-secondary">Em Andamento</span> e se o usuário tiver permissão.</li>
  <li><strong>Botão "Baixar todo processo (ZIP)"</strong> — permite gerar um pacote único com todos os PDFs daquele processo.</li>
</ul>

<h5 class="mt-4">Permissões de Acesso</h5>
<p>
  A página de andamento é acessível apenas por:
</p>
<ul>
  <li>Administradores do sistema ou do Departamento;</li>
  <li>Usuários designados especificamente ao processo (vinculados);</li>
</ul>

<div class="alert alert-info">
  Se você não tiver permissão para visualizar determinado processo, o sistema impedirá o acesso automaticamente por razões de segurança e sigilo funcional.
</div>

<h5 class="mt-4">Dica:</h5>
<p>
  Use os botões de <i class="bi bi-clipboard"></i> para copiar rapidamente dados como o NUP ou o nome do Polo Passivo, otimizando o preenchimento de formulários ou comunicação oficial.
</p>
