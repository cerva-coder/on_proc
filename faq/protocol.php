<h3><i class="bi bi-upload text-success"></i> Protocolar Documento</h3>
<hr>

<p>
  A etapa de <strong>protocolo de documentos</strong> tem por objetivo registrar formalmente movimentações no processo em andamento, anexando arquivos e informações descritivas que ficarão armazenadas no histórico daquele procedimento.
</p>

<h5 class="mt-4">Quem pode protocolar?</h5>
<ul>
  <li><strong>Administrador do Sistema</strong></li>
  <li><strong>Administrador do Departamento (Gerente)</strong> do Departamento responsável</li>
  <li><strong>Usuários diretamente atribuídos ao processo</strong> (designados pela Administração)</li>
</ul>

<div class="alert alert-warning">
  <strong>Importante:</strong> O processo precisa estar com status <span class="badge bg-secondary">Em Andamento</span> para que novos protocolos possam ser inseridos.
</div>

<h5 class="mt-4">Requisitos do Documento</h5>
<ul>
  <li>Deve ser enviado um <strong>arquivo PDF</strong> válido, com até <strong>50 MB</strong>.</li>
  <li>É obrigatório preencher o <strong>título da movimentação</strong> e uma <strong>descrição do documento</strong> anexado.</li>
  <li>O usuário deverá <strong>confirmar sua senha</strong> antes de concluir o envio, como medida de segurança.</li>
</ul>

<h5 class="mt-4">Criptografia e Armazenamento</h5>
<p>
  Todos os documentos enviados são <strong>criptografados com AES-256-CBC</strong> antes de serem armazenados no servidor. Isso garante a confidencialidade das informações sensíveis protocoladas.
</p>

<h5 class="mt-4">Atenção:</h5>
<div class="alert alert-danger">
  O protocolo é <strong>IRREVERSÍVEL</strong>.<br>
  Após enviado, o documento somente poderá ser removido por um <strong>Administrador Geral</strong> do sistema.
</div>

<h5 class="mt-4">Boa Prática:</h5>
<p>
  Certifique-se de que o conteúdo do PDF corresponde fielmente à movimentação descrita e que todas as informações estão corretas antes do envio.
</p>
